def fnv1a32(data):
    h = 0x811c9dc5
    for b in data:
        h ^= b
        h = (h * 0x1000193) & 0xffffffff
    return h

def xorshift32(x):
    x ^= (x << 13) & 0xffffffff
    x ^= (x >> 17) & 0xffffffff
    x ^= (x << 5) & 0xffffffff
    return x & 0xffffffff

k = bytes([
0x13,0x37,0xc0,0xde,0x42,0x99,0xaa,0x10,
0x01,0x02,0x03,0x04,0x55,0x66,0x77,0x88
])

ct = [
0xd2,0xc8,0x41,0x0f,0xfd,0xd4,0xaf,0x88,
0x03,0xc1,0xa0,0x50,0xdc,0xac,0xb5,0x1e,
0x42,0x06,0x3a,0x55
]

seed = fnv1a32(k)

flag = []

for i in range(len(ct)):
    seed = xorshift32(seed)
    flag.append(seed & 0xff ^ ct[i])

print(bytes(flag))
