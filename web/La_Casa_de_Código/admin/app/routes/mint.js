// Atbash cipher helper 
// Atbash: A↔Z, B↔Y, C↔X, ...  (preserves case)
function atbash(str) {
  return str.split('').map(c => {
    if (c >= 'a' && c <= 'z') {
      // a(97)+z(122) = 219;  atbash(c) = 219 - charCode
      return String.fromCharCode(219 - c.charCodeAt(0));
    }
    if (c >= 'A' && c <= 'Z') {
      // A(65)+Z(90) = 155;  atbash(C) = 155 - charCode
      return String.fromCharCode(155 - c.charCodeAt(0));
    }
    return c;
  }).join('');
}

